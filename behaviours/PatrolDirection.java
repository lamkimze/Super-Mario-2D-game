package game.behaviours;

/**
 * An enum class for the PatrolDirection along the patrolLocations ArrayList
 * @author Rory-Tobin Underwood
 * @version 1
 */
public enum PatrolDirection {
    /**
     * Means the actor moves along the patrolLocations in the right direction
     */
    RIGHT,
    /**
     * Means the actor moves along the patrolLocations in the left direction
     */
    LEFT
}
